# first string value
a = input("Enter a string value: ")
print(f"Your first string value is: {a}")

# second string value
b = input("Enter another string value: ")
print(f"Your second string value is: {b}")

# number value
c = int(input("Enter a number (preferably less than 10): "))
print(f"You entered number: {c}")

# calculation
print(f"{a} + {b} is", a + b)
print(f"{a} * {c} is", a * c)
print(f"{b} * {c} is", b * c)


